# belajar_operator.py
# Contoh penggunaan operator dalam Python
# Operator digunakan untuk melakukan operasi pada data

# Operator aritmatika
x = 10
y = 3
print(x + y)  # Penjumlahan
print(x - y)  # Pengurangan
print(x * y)  # Perkalian
print(x / y)  # Pembagian
print(x % y)  # Sisa bagi

# Operator perbandingan
print(x > y)  # Lebih besar
print(x == y) # Sama dengan
print(x != y) # Tidak sama dengan
