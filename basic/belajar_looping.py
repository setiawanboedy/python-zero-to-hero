# belajar_looping.py
# Contoh penggunaan looping (perulangan) dalam Python
# Looping digunakan untuk mengulang eksekusi kode beberapa kali

# Menggunakan for
for i in range(5):
    print("Perulangan ke-", i)  # Menampilkan nomor perulangan

# Menggunakan while
count = 0
while count < 3:
    print("Hitung ke-", count)  # Menampilkan hitungan
    count += 1
