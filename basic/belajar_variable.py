# Contoh penggunaan variabel dalam Python
# Variabel digunakan untuk menyimpan data

nama = "Andi"           # Menyimpan teks ke dalam variabel
umur = 20                # Menyimpan angka ke dalam variabel
is_mahasiswa = True      # Menyimpan nilai boolean ke dalam variabel

print(nama)              # Menampilkan isi variabel nama
print(umur)              # Menampilkan isi variabel umur
print(is_mahasiswa)      # Menampilkan isi variabel is_mahasiswa
