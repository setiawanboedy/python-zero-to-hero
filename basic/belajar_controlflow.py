# belajar_controlflow.py
# Contoh penggunaan control flow (percabangan) dalam Python
# Control flow digunakan untuk mengatur alur program berdasarkan kondisi tertentu

nilai = 75
if nilai >= 80:
    print("Nilai A")  # Jika nilai lebih dari atau sama dengan 80
elif nilai >= 70:
    print("Nilai B")  # Jika nilai lebih dari atau sama dengan 70
else:
    print("Nilai C")  # Jika nilai kurang dari 70
