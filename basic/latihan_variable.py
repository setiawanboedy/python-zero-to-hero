# latihan_variable.py
# Latihan sederhana penggunaan variabel

hobi = "Membaca"
tahun_lahir = 2000
print("Hobi saya:", hobi)
print("Tahun lahir:", tahun_lahir)

# Contoh medium: update nilai variabel
umur = 25
umur = umur + 1  # Menambah umur satu tahun
print("Umur setelah ulang tahun:", umur)

# Contoh medium: operasi antar variabel
panjang = 5
lebar = 3
luas = panjang * lebar
print("Luas persegi panjang:", luas)

# Contoh medium: multiple assignment dan unpacking
a, b, c = 1, 2, 3
print("a:", a, "b:", b, "c:", c)
