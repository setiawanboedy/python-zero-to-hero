# latihan_operator.py
# Latihan sederhana penggunaan operator

a = 7
b = 2
print("Hasil a + b:", a + b)
print("Hasil a * b:", a * b)
print("a lebih besar dari b:", a > b)

# Contoh medium: operator logika
x = 5
y = 10
print("x > 3 and y < 15:", x > 3 and y < 15)
print("x < 3 or y > 5:", x < 3 or y > 5)
print("not(x == 5):", not(x == 5))

# Contoh medium: operator gabungan (augmented assignment)
z = 8
z += 2  # z = z + 2
print("Nilai z setelah += 2:", z)
z *= 3  # z = z * 3
print("Nilai z setelah *= 3:", z)

# Contoh medium: operasi pada string
teks1 = "Halo"
teks2 = "Dunia"
print(teks1 + " " + teks2)  # Penggabungan string
print(teks1 * 3)            # Mengulang string
