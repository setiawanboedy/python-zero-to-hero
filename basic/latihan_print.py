# latihan_print.py
# Latihan sederhana penggunaan print

print("Nama saya Budi")
print("Saya sedang belajar Python")
print(2025)

# Contoh medium: print dengan format string
nama = "Siti"
umur = 22
print("Nama saya {} dan umur saya {} tahun".format(nama, umur))

# Contoh medium: print dengan f-string
print(f"Nama: {nama}, Umur: {umur}")

# Contoh medium: print dengan karakter khusus (escape character)
print("Baris pertama\nBaris kedua")
print("Dia berkata: \"Halo!\"")

# Contoh medium: print beberapa variabel sekaligus
x = 10
y = 20
print("Nilai x:", x, "dan y:", y)
